#import "NSColor+RSSClient.h"

@implementation NSColor (RSSClient)

+ (NSColor *)darkGreenColor
{
    return [self colorWithCalibratedRed:0.0 green:0.5 blue:0.0 alpha:1.0];
}

+ (NSColor *)darkVioletColor
{
    return [self colorWithCalibratedRed:0.25 green:0.0 blue:0.5 alpha:1.0];
}

@end
